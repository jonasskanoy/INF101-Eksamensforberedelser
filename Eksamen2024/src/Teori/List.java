// List
// List is an interface in the Java Collections Framework that provides a way to store an ordered collection of elements. 
// It is a subinterface of the Collection interface and has several implementations, including ArrayList, LinkedList, and Vector.

// Characteristics of List:
// Order: List maintains the order of elements based on the insertion order.
// Duplicates: It allows duplicate elements.
// Positional Access: Provides methods to access elements based on their position (index) in the list.
// Flexibility: Being an interface, List does not specify how the elements are stored or accessed. 
// It can be implemented by various classes that define specific behaviors.

// Common Methods in List:
// void add(int index, E element): Inserts the specified element at the specified position.
// boolean add(E element): Appends the specified element to the end of the list.
// E get(int index): Returns the element at the specified position.
// E remove(int index): Removes the element at the specified position.
// int size(): Returns the number of elements in the list.
// List<E> subList(int fromIndex, int toIndex): Returns a view of the portion of this list between the specified indices.

