package Teori;

public class Iterable {
// Iterable
// The Iterable interface represents a collection of objects that can be traversed one at a time. 
// It is part of the Java Collections Framework and is implemented by all collection classes, 
// such as ArrayList, LinkedList, HashSet, and TreeSet. 
// The primary purpose of the Iterable interface is to provide a way to obtain an Iterator for the collection.

// Characteristics of Iterable:
// Method: The Iterable interface has a single method, iterator(), which returns an Iterator over elements of type T.
// Enhanced For-Loop: Implementing Iterable allows an object to be the target of the enhanced for-loop 
// (also known as the "for-each" loop).
// Multiple Iterators: The iterator() method can be called multiple times to obtain multiple Iterator objects.
}
